package YunSuan;

public class MathDemo {
  public static void main(String[] args) {
    // 定义两个变量
    int a = 3;
    int b = 4;
    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);
    // 整数相除只能得到整数，要想得到小数，就必须有浮点数参与运算
    System.out.println(3 / 4.0);
    System.out.println(3.0 / 4);
  }
}
